
public class Book
{
   
   int id;
   String title;
   int price;
   String author;
   int count=0;
public int getId() 
{
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getTitle() {
	return title;
}
public void setTitle(String title) {
	this.title = title;
}
public int getPrice() 
{
	return price;
}
public void setPrice(int price)
{
	this.price = price;
}
public String getAuthor() 
{
	return author;
}
public void setAuthor(String author) 
{
	this.author = author;
}
public int getCount() 
{
	return count;
}
public void setCount(int count) 
{
	this.count = count;
}
   
   
}
